/**
 * SLIDER
 */

const sliders = document.querySelectorAll("[data-slider]");

const initSlider = function (currentSlider) {

  const sliderContainer = currentSlider.querySelector("[data-slider-container]");
  const sliderPrevBtn = currentSlider.querySelector("[data-slider-prev]");
  const sliderNextBtn = currentSlider.querySelector("[data-slider-next]");

  let totalSliderVisibleItems = Number(getComputedStyle(currentSlider).getPropertyValue("--slider-items"));
  let totalSlidableItems = sliderContainer.childElementCount - totalSliderVisibleItems;

  let currentSlidePos = 0;

  const moveSliderItem = function () {
    sliderContainer.style.transform = `translateX(-${sliderContainer.children[currentSlidePos].offsetLeft}px)`;
  }

  /**
   * NEXT SLIDE
   */
  const slideNext = function () {
    const slideEnd = currentSlidePos >= totalSlidableItems;

    if (slideEnd) {
      currentSlidePos = 0;
    } else {
      currentSlidePos++;
    }

    moveSliderItem();
  }

  sliderNextBtn.addEventListener("click", slideNext);

  /**
   * PREVIOUS SLIDE
   */
  const slidePrev = function () {
    if (currentSlidePos <= 0) {
      currentSlidePos = totalSlidableItems;
    } else {
      currentSlidePos--;
    }

    moveSliderItem();
  }

  sliderPrevBtn.addEventListener("click", slidePrev);

  const dontHaveExtraItem = totalSlidableItems <= 0;
  if (dontHaveExtraItem) {
    sliderNextBtn.style.display = 'none';
    sliderPrevBtn.style.display = 'none';
  }

  /**
   * slide with [shift + mouse wheel]
   */

  currentSlider.addEventListener("wheel", function (event) {
    if (event.shiftKey && event.deltaY > 0) slideNext();
    if (event.shiftKey && event.deltaY < 0) slidePrev();
  });

  /**
   * RESPONSIVE
   */

  window.addEventListener("resize", function () {
    totalSliderVisibleItems = Number(getComputedStyle(currentSlider).getPropertyValue("--slider-items"));
    totalSlidableItems = sliderContainer.childElementCount - totalSliderVisibleItems;

    moveSliderItem();
  });

}

for (let i = 0, len = sliders.length; i < len; i++) { initSlider(sliders[i]); }

/*=============== DARK LIGHT THEME ===============*/

const button = document.getElementById('theme-button');
const body = document.body;
const sunIconClass = 'bx-sun'; // Class for sun icon
const moonIconClass = 'bx-moon'; // Class for moon icon

function toggleTheme() {
  body.classList.toggle('dark-mode'); // Toggle dark mode class on body

  // Update icons based on theme state
  if (body.classList.contains('dark-mode')) {
    button.classList.remove(moonIconClass); // Remove moon if dark mode
    button.classList.add(sunIconClass); // Add sun icon for dark mode
  } else {
    button.classList.remove(sunIconClass); // Remove sun if light mode
    button.classList.add(moonIconClass); // Add moon icon for light mode
  }

  // Improve transition speed (optional)
  body.style.transition = 'background-color 0.3s ease-in-out'; // Set transition for smoother change
}

button.addEventListener('click', toggleTheme);

// Check initial theme and set corresponding icon
if (body.classList.contains('dark-mode')) {
  button.classList.add(sunIconClass); // Add sun icon for initial dark mode (opposite behavior)
} else {
  button.classList.add(moonIconClass); // Add moon icon for initial light mode
}